    private void jbAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAgregarActionPerformed
        // Agregar
        try {
            if (this.jtImporteDiario.getText().isEmpty() || this.jdateFechaIngre.getDate() == null || this.jdateFechaSalida.getDate() == null) {
                JOptionPane.showMessageDialog(null, "No deben quedar campos vac√≠os", "Loco F√≠jate", JOptionPane.ERROR_MESSAGE);
                return;
            }
            Ciudad ciu = (Ciudad) this.jcCiuDestino.getSelectedItem();
            java.util.Date fIng = this.jdateFechaIngre.getDate(); // Obtener la fecha de ingreso como java.util.Date
            java.util.Date fSal = this.jdateFechaSalida.getDate(); // Obtener la fecha de salida como java.util.Date
            if (fIng.after(fSal)) {
                JOptionPane.showMessageDialog(null, "La fecha de ingreso tiene que ser antes de la fecha de salida", "Loco F√≠jate", JOptionPane.ERROR_MESSAGE);
                limpiar();
                return;
            }
            TipoAlojamiento alo = (TipoAlojamiento) this.jcAlo.getSelectedItem();
            Servicio ser = (Servicio) this.jcServicio.getSelectedItem();
            double imp = Double.parseDouble(this.jtImporteDiario.getText());
            if(imp<0){
                JOptionPane.showMessageDialog(null, "Para el importe debe agregar n√∫meros positivos");
                limpiar();
            }else{
                
            
            boolean est = true;
            // Convertir java.util.Date a java.sql.Date
            java.sql.Date sqlDateIng = new java.sql.Date(fIng.getTime());
            java.sql.Date sqlDateSal = new java.sql.Date(fSal.getTime());
            // Crear un objeto Alojamiento usando las fechas convertidas a java.sql.Date
            Alojamiento alojamiento = new Alojamiento(ciu, sqlDateIng.toLocalDate(), sqlDateSal.toLocalDate(), alo, ser, imp, est);
            AlojamientoDatos.alojamientoGuardar(alojamiento);
            limpiar();
            modelo.setRowCount(0);
            cargarTabla();
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Solo n√∫meros positivos" + e.getMessage(), "Date cuenta capo solo NUMEROS", JOptionPane.ERROR_MESSAGE);
            limpiar();
        }
        textMod(false);
    }//GEN-LAST:event_jbAgregarActionPerformed

